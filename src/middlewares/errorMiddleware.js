const ENV = process.env.NODE_ENV;

export default function errorMiddleware(err, req, res, next){
    if (err) {
      if (ENV === "production") {
        res.status(500).send({ error: "Algo deu errado..." });
      } else {
        res.status(500).send({ error: err });
      }
      console.log(err);
    }
  };
